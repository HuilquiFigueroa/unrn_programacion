lista_de_compras = ["pan", "leche", "huevos"]
print(lista_de_compras[0])
print(lista_de_compras[-1])
print(len(lista_de_compras))
# Al printear estas expresiones, con el [0] nos muestra el pan, porque el codigo ejecuta
# el primer elemento de la lista, lo mismo sucede con el [-1], que ejecuta el codigo y
# nos arroja el ultimo elemento de la lista.
# al printear len(lista), nos puede mostrar la cantidad total de elementos de una lista